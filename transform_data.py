# ── Core movies table ─────────────────────────────────────────────────────────

def build_movies(df: pd.DataFrame) -> pd.DataFrame:
    """
    Build the main movies table.

    Derived columns:
      profit          – revenue - budget  (null when either is 0 / unknown)
      roi             – profit / budget   (null when budget is 0)
      release_year    – year extracted from release_date
      release_month   – month extracted from release_date
      vote_weighted   – Bayesian-weighted rating, shrinks towards global mean
                        for movies with very few votes (m = 500 vote threshold)
    """
    m = df[[
        "movie_id", "title", "original_title", "original_language",
        "overview", "tagline", "status", "homepage",
        "budget", "revenue", "runtime",
        "popularity", "vote_average", "vote_count",
        "release_date",
    ]].copy()

    # Replace 0 with NaN for financial columns (0 means "unknown" in TMDB)
    m["budget"]  = m["budget"].replace(0, np.nan)
    m["revenue"] = m["revenue"].replace(0, np.nan)

    # Financial derived features
    m["profit"] = m["revenue"] - m["budget"]
    m["roi"]    = (m["profit"] / m["budget"]).replace([np.inf, -np.inf], np.nan)

    # Date features
    dates = pd.to_datetime(m["release_date"], errors="coerce")
    m["release_year"]  = dates.dt.year
    m["release_month"] = dates.dt.month

    # Bayesian-weighted vote score
    # Formula: (v / (v + m)) * R + (m / (v + m)) * C
    # where v = vote_count, m = 500 (minimum votes threshold),
    #       R = movie vote_average, C = global mean vote_average
    C = m["vote_average"].mean()
    min_votes = 500
    v = m["vote_count"]
    R = m["vote_average"]
    m["vote_weighted"] = ((v / (v + min_votes)) * R + (min_votes / (v + min_votes)) * C).round(4)

    m = m.sort_values("movie_id").reset_index(drop=True)
    print(f"  movies: {len(m):>6} rows")
    return m


