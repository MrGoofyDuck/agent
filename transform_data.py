# ── Cast table ────────────────────────────────────────────────────────────────

def build_cast(df: pd.DataFrame) -> pd.DataFrame:
    """
    Explode the cast JSON array into one row per actor per movie.

    Columns:
      movie_id, person_id, name, characters, cast_order, gender

    cast_order=0 is the top-billed actor. Only the top 15 billed
    actors are kept to avoid huge tables for ensemble casts.
    """
    MAX_CAST = 15
    records = []
    for _, row in df[["movie_id", "cast"]].iterrows():
        for actor in safe_json(row["cast"]):
            if actor.get("order", 999) >= MAX_CAST:
                continue
            records.append({
                "movie_id":   row["movie_id"],
                "person_id":  actor["id"],
                "name":       actor["name"],
                "characters":  actor.get("character", ""),
                "cast_order": actor.get("order", None),
                "gender":     actor.get("gender", 0),  # 0=unknown,1=female,2=male
            })

    cast = (
        pd.DataFrame(records)
        .drop_duplicates(subset=["movie_id", "person_id"])
        .sort_values(["movie_id", "cast_order"])
        .reset_index(drop=True)
    )
    print(f"  cast: {len(cast):>7} rows (top {MAX_CAST} actors per movie)")
    return cast


