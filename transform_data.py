# ── Genre reference + bridge ──────────────────────────────────────────────────

def build_genres(df: pd.DataFrame) -> tuple[pd.DataFrame, pd.DataFrame]:
    """
    Parse genres JSON -> deduplicated reference table + movie_genres bridge.
    """
    records = []
    for _, row in df[["movie_id", "genres"]].iterrows():
        for g in safe_json(row["genres"]):
            records.append({"genre_id": g["id"], "genre_name": g["name"], "movie_id": row["movie_id"]})

    all_genres = pd.DataFrame(records)

    genres_ref = (
        all_genres[["genre_id", "genre_name"]]
        .drop_duplicates("genre_id")
        .sort_values("genre_id")
        .reset_index(drop=True)
    )

    movie_genres = (
        all_genres[["movie_id", "genre_id"]]
        .drop_duplicates()
        .sort_values(["movie_id", "genre_id"])
        .reset_index(drop=True)
    )

    print(f"  genres:        {len(genres_ref):>6} unique genres")
    print(f"  movie_genres:  {len(movie_genres):>6} rows")
    return genres_ref, movie_genres


genres_ref, movie_genres = build_genres(raw)
