# ── Crew table ────────────────────────────────────────────────────────────────

def build_crew(df: pd.DataFrame) -> pd.DataFrame:
    """
    Explode the crew JSON array into one row per crew member per movie.
    Only key departments are kept to focus on decision-makers:
      Directing, Writing, Production, and Visual Effects.

    Columns:
      movie_id, person_id, name, department, job, gender
    """
    KEY_DEPARTMENTS = {"Directing", "Writing", "Production", "Visual Effects"}

    records = []
    for _, row in df[["movie_id", "crew"]].iterrows():
        for member in safe_json(row["crew"]):
            dept = member.get("department", "")
            if dept not in KEY_DEPARTMENTS:
                continue
            records.append({
                "movie_id":   row["movie_id"],
                "person_id":  member["id"],
                "name":       member["name"],
                "department": dept,
                "job":        member.get("job", ""),
                "gender":     member.get("gender", 0),
            })

    crew = (
        pd.DataFrame(records)
        .drop_duplicates(subset=["movie_id", "person_id", "job"])
        .sort_values(["movie_id", "department", "job"])
        .reset_index(drop=True)
    )
    print(f"  crew: {len(crew):>7} rows (Directing/Writing/Production/VFX only)")
    return crew


