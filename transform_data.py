def transform_data() -> None:
    """Run full Step 2: load -> parse -> derive -> split -> save."""
    print(f"=== transform_data | input: {INPUT_FILE} ===\n")

    raw = load_raw()

    print("\nBuilding tables:")
    movies_tbl                    = build_movies(raw)
    genres_ref, movie_genres      = build_genres(raw)
    keywords_ref, movie_keywords  = build_keywords(raw)
    companies_ref, movie_companies = build_companies(raw)
    cast_tbl                      = build_cast(raw)
    crew_tbl                      = build_crew(raw)

    outputs = {
        "table_movies.csv":               movies_tbl,
        "table_genres.csv":               genres_ref,
        "table_movie_genres.csv":         movie_genres,
        "table_keywords.csv":             keywords_ref,
        "table_movie_keywords.csv":       movie_keywords,
        "table_production_companies.csv": companies_ref,
        "table_movie_companies.csv":      movie_companies,
        "table_cast.csv":                 cast_tbl,
        "table_crew.csv":                 crew_tbl,
    }

    print("\nSaving:")
    for fname, frame in outputs.items():
        frame.to_csv(fname, index=False)
        print(f"  {fname}")

    print("\nDone. Next step: load_to_mysql.py")


